private void cargarTabla() {
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID");
    modelo.addColumn("Código");
    modelo.addColumn("Nombre");
    modelo.addColumn("Bloque");
    modelo.addColumn("Sección");

    String bloqueBuscado = Bloque.getText().trim(); // <-- Lo que escribas en el JTextField

    try {
        Connection con = Conexion.obtenerConexion();
        String sql;

        if (bloqueBuscado.isEmpty()) {
            // Si no escribió nada, mostrar todos
            sql = "SELECT ID_lab, Codigo_lab, Nombre_lab, Bloque, Seccion FROM laboratorios WHERE estadolab = 1 ";
        } else {
            // Si escribió un bloque, buscar solo ese bloque
            sql = "SELECT ID_lab, Codigo_lab, Nombre_lab, Bloque, Seccion FROM laboratorios WHERE Bloque = ? AND estadolab = 1 ";
        }

        PreparedStatement pst = con.prepareStatement(sql);

        if (!bloqueBuscado.isEmpty()) {
            pst.setString(1, bloqueBuscado);
        }

        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            Object[] fila = new Object[5];
            fila[0] = rs.getInt("ID_lab");
            fila[1] = rs.getString("Codigo_lab");
            fila[2] = rs.getString("Nombre_lab");
            fila[3] = rs.getString("Bloque");
            fila[4] = rs.getString("Seccion");
            modelo.addRow(fila);
        }

        VerLaboratorios.setModel(modelo);

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al cargar laboratorios: " + e.getMessage());
    }
}


