    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        int filaSeleccionada = VerLaboratorios.getSelectedRow();
        if (filaSeleccionada != -1) {
            String codigoLab = VerLaboratorios.getValueAt(filaSeleccionada, 1).toString(); // columna Código Laboratorio
            campoDestino.setText(codigoLab);
            dispose(); // Cierra la ventana después de seleccionar
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un laboratorio para guardar.");
        }
    }//GEN-LAST:event_GuardarActionPerformed

