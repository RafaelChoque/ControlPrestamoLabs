    private void cargarTablaFiltrada(String ru, String seccion, String estado, Date fechaDesde, Date fechaHasta) {
        try {
            Connection con = Conexion.obtenerConexion();

            String query = "SELECT p.id_prestamo, l.Nombre_lab, CONCAT(pa.nombre, ' ', pa.apellido) AS docente, "
                    + "l.Seccion, p.fecha, p.horario_inicio, p.horario_fin, p.estado "
                    + "FROM prestamos p "
                    + "INNER JOIN laboratorios l ON p.ID_lab = l.ID_lab "
                    + "INNER JOIN personal_academico pa ON p.id_personal_academico = pa.id_personal_academico "
                    + "WHERE 1=1";

            List<Object> parametros = new ArrayList<>();

            if (ru != null && !ru.isEmpty()) {
                query += " AND pa.RU = ?";
                parametros.add(ru);
            }
            if (seccion != null && !"Todos".equals(seccion)) {
                query += " AND l.Seccion = ?";
                parametros.add(seccion);
            }
            if (estado != null && !"Todos".equals(estado)) {
                query += " AND p.estado = ?";
                parametros.add(estado);
            }

            if (fechaDesde != null && fechaHasta != null) {
                query += " AND p.fecha BETWEEN ? AND ?";
                parametros.add(new java.sql.Date(fechaDesde.getTime()));
                parametros.add(new java.sql.Date(fechaHasta.getTime()));
            }

            PreparedStatement ps = con.prepareStatement(query);

            for (int i = 0; i < parametros.size(); i++) {
                ps.setObject(i + 1, parametros.get(i));
            }

            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) tblReportes.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("id_prestamo"),
                        rs.getString("docente"),
                        rs.getString("Seccion"),
                        rs.getString("Nombre_Lab"),
                        rs.getDate("fecha"),
                        rs.getString("horario_inicio"),
                        rs.getString("horario_fin"),
                        rs.getString("estado"),
                });
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar datos filtrados.");
        }
    }
    @Override
    public int print(Graphics graphics, PageFormat pageFormat, int pageIndex) throws PrinterException {
        if (pageIndex > 0) {
            return NO_SUCH_PAGE;
        }

        // Usamos márgenes estrechos: ya definidos en setPrintable()
        Graphics2D g2d = (Graphics2D) graphics;
        g2d.translate(pageFormat.getImageableX(), pageFormat.getImageableY());

        // Escalar si el contenido es muy grande
        g2d.scale(0.8, 0.8);

        // Fondo blanco (opcional si ya es blanco)
        g2d.setColor(Color.WHITE);
        g2d.fillRect(0, 0, jPanel1.getWidth(), jPanel1.getHeight());

        // Pintar solo contenido sin estilos adicionales
        jPanel1.printAll(g2d);

        return PAGE_EXISTS;
    }
    private void ImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImprimirActionPerformed
        Imprimir.setVisible(false);

        PrinterJob printerJob = PrinterJob.getPrinterJob();

        // Configuración de página con márgenes estrechos y horizontal
        PageFormat format = printerJob.defaultPage();
        format.setOrientation(PageFormat.LANDSCAPE);

        Paper paper = format.getPaper();
        double margin = 20; // 20 puntos ≈ 0.7 cm

        double width = paper.getWidth();
        double height = paper.getHeight();
        paper.setImageableArea(margin, margin, width - 2 * margin, height - 2 * margin);
        format.setPaper(paper);

        printerJob.setPrintable(this, format);

        if (printerJob.printDialog()) {
            try {
                printerJob.print();
            } catch (PrinterException ex) {
                ex.printStackTrace();
            }
        }

        Imprimir.setVisible(true);
        dispose();
    }//GEN-LAST:event_ImprimirActionPerformed

